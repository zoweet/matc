
public class StringFun {
	public static void main(String[] args)
	   {
	      String myName = "Roberta";
	      int nameLength = 6 ;

	      char firstLetter = myName.charAt(0);
	      char lastLetter = myName.charAt(6);

	      System.out.println("My name is: \t\t" + myName);
	      System.out.println("The first letter is: \t" + firstLetter);
	      System.out.println("The last letter is: \t" + lastLetter);
	      System.out.println("My name is " + nameLength + " letters long");
	   }
	}

